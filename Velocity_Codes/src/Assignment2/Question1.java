public class Question1 {
	public static void main(String[] args) {
		
		boolean a = true;  				//Boolean Data Type
		
		byte by = 100;                    // byte data type. this can store only numbers between -128 to 127
		
		char ch = 'S' ;							// char data type. this can store characters such as A or s.
		
		short st = 4000;                   // short data type it saves values ranging from -32,768 to 32767
		
		int i = 4;                              // integer data type. it saves numbers from -2147483648 to 2147483647
		
		long num = 15000000000L;
		
		float fl =  8.484f;                  //saves numbers ranging from 3.4e038 to 3.4e+038 
		
		double du = 4.488;            // saves numbers from 1.7e-308 to 1.7e+308
		
	}
}
